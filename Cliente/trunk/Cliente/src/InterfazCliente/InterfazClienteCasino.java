    private void botonApostarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonApostarActionPerformed
        if(labelValor.getText().equals("---"))
            JOptionPane.showMessageDialog(this,"DEBE ELEGIR SU OPCION DE APUESTA","Fallo en la apuesta",JOptionPane.WARNING_MESSAGE );
        else{
            if(labelTotalApostado.getText().equals("0"))
                JOptionPane.showMessageDialog(this,"EL VALOR DE LA APUESTA DEBE SER MAYOR QUE 0","Fallo en la apuesta",JOptionPane.WARNING_MESSAGE );
            else{
                Apuesta apuesta = new Apuesta(Integer.parseInt(labelTotalApostado.getText()),labelValor.getText());
            }
        }
    }//GEN-LAST:event_botonApostarActionPerformed

